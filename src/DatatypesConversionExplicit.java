public class DatatypesConversionExplicit {
    public static void main(String[] args) {
        double g = 250.56;
        System.out.println("double representation: "+g);
        float f =(float)g;
        System.out.println("float representation: "+f);
        long h =(long)g;
        System.out.println("long representation: "+h);
        int i =(int)h;
        System.out.println("int representation: "+i);
    }
}
